package com.abs.ps.web.dto;


public class CustomerDto {
	private Long oid;
	private String name;
	
	private String contactPerson;
	private String mobileNum;
	private String email;
	private String status;
	private String createBy;
	private String createDate;
	
	private long departOid;
	private boolean deletable;
	public Long getOid() {
		return oid;
	}
	public void setOid(Long oid) {
		this.oid = oid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public String getContactPerson() {
		return contactPerson;
	}
	public void setContactPerson(String contactPerson) {
		this.contactPerson = contactPerson;
	}
	public String getMobileNum() {
		return mobileNum;
	}
	public void setMobileNum(String mobileNum) {
		this.mobileNum = mobileNum;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCreateBy() {
		return createBy;
	}
	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public String getCreateDate() {
		return createDate;
	}
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	
	public long getDepartOid() {
		return departOid;
	}
	public void setDepartOid(long departOid) {
		this.departOid = departOid;
	}
	public boolean isDeletable() {
		return deletable;
	}
	public void setDeletable(boolean deletable) {
		this.deletable = deletable;
	}
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((oid == null) ? 0 : oid.hashCode());
		return result;
	}
	
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		CustomerDto other = (CustomerDto) obj;
		if (oid == null) {
			if (other.oid != null)
				return false;
		} else if (!oid.equals(other.oid))
			return false;
		return true;
	}
	
	@Override
	public String toString() {
		return "CustomerInfoDto [oid=" + oid + ", name=" + name
				+ ", contactPerson=" + contactPerson + ", mobileNum=" + mobileNum + ", email=" + email + ", status="
				+ status + ", createBy=" + createBy + ", createDate=" + createDate + ", departOid=" + departOid + "]";
	}
	
	
	
}
