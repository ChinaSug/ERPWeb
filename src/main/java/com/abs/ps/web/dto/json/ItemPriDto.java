package com.abs.ps.web.dto.json;

public class ItemPriDto {
	
	private String value;
	private String text;
	private String unitPrice;
	private String damPrice;
	private String matPrice;
	private String piePrice;
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getUnitPrice() {
		return unitPrice;
	}
	public void setUnitPrice(String unitPrice) {
		this.unitPrice = unitPrice;
	}
	public String getDamPrice() {
		return damPrice;
	}
	public void setDamPrice(String damPrice) {
		this.damPrice = damPrice;
	}
	public String getMatPrice() {
		return matPrice;
	}
	public void setMatPrice(String matPrice) {
		this.matPrice = matPrice;
	}
	public String getPiePrice() {
		return piePrice;
	}
	public void setPiePrice(String piePrice) {
		this.piePrice = piePrice;
	}

}
